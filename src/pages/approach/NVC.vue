        <h1>Non-Violent Communication (NVC)</h1>

        <p class="lead text-xl text-slate-600">A language of compassion that centers universal human needs and teaches us to make clear, connecting requests—transforming how we speak, listen, and relate.</p>

        <p>Developed by psychologist Marshall Rosenberg, Non-Violent Communication offers a framework for honest expression and empathic listening. It's not about being "nice" or avoiding conflict—it's about speaking truth in ways that build connection rather than walls. NVC helps us move past blame, criticism, and demand to reveal the human needs at the heart of every interaction.</p>

        <p>The approach rests on a simple but profound insight: beneath every action, every word, every conflict lies an attempt to meet universal human needs. When we learn to identify and speak from these needs—rather than from judgments about others—we open pathways to understanding that seemed impossible before.</p>

